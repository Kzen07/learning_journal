let expenses = [];

function addExpense() {
    const amount = +prompt("–í–≤–µ–¥–∏—Ç–µ —Å—É–º–º—É —Ä–∞—Å—Ö–æ–¥–∞:");
    if (isNaN(amount) || amount <= 0) {
        console.log("‚ùå –û—à–∏–±–∫–∞: –≤–≤–µ–¥–∏—Ç–µ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–µ —á–∏—Å–ª–æ!");
        return;
    }

    const category = prompt("–í–≤–µ–¥–∏—Ç–µ –∫–∞—Ç–µ–≥–æ—Ä–∏—é (–µ–¥–∞, —Ç—Ä–∞–Ω—Å–ø–æ—Ä—Ç –∏ —Ç.–¥.):") || "–¥—Ä—É–≥–æ–µ";

    expenses.push({ amount, category });
    console.log(`‚úÖ –î–æ–±–∞–≤–ª–µ–Ω–æ: ${amount} ‚Ç∏ ‚Äî ${category}`);
}

function showExpenses() {
    if (expenses.length === 0) {
        console.log("üì≠ –†–∞—Å—Ö–æ–¥–æ–≤ –ø–æ–∫–∞ –Ω–µ—Ç.");
        return;
    }

    console.log("üìã –°–ø–∏—Å–æ–∫ —Ä–∞—Å—Ö–æ–¥–æ–≤:");
    for (let i = 0; i < expenses.length; i++) {
        console.log(`${i + 1}. ${expenses[i].amount} ‚Ç∏ ‚Äî ${expenses[i].category}`);
    }
}

function calculateTotal() {
    let total = 0;
    for (let i = 0; i < expenses.length; i++) {
        total += expenses[i].amount;
    }
    return total;
}

function deleteExpense() {
    const index = +prompt("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–º–µ—Ä —Ä–∞—Å—Ö–æ–¥–∞ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è:") - 1;

    if (index >= 0 && index < expenses.length) {
        const deleted = expenses.splice(index, 1)[0];
        console.log(`üóë –£–¥–∞–ª–µ–Ω–æ: ${deleted.amount} ‚Ç∏ ‚Äî ${deleted.category}`);
    } else {
        console.log("‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π –Ω–æ–º–µ—Ä!");
    }
}

function showByCategory() {
    const categoryTotals = {};

    for (let i = 0; i < expenses.length; i++) {
        const cat = expenses[i].category;
        categoryTotals[cat] = (categoryTotals[cat] || 0) + expenses[i].amount;
    }

    console.log("üìä –†–∞—Å—Ö–æ–¥—ã –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º:");
    for (const cat in categoryTotals) {
        console.log(`${cat}: ${categoryTotals[cat]} ‚Ç∏`);
    }
}

